function opt(values) {
  return values.map((text, index) => ({ key: String.fromCharCode(65 + index), text }))
}

function single(tag, stem, options, answer, explanation) {
  return {
    type: 'single',
    stem,
    options: opt(options),
    correctAnswers: [answer],
    tags: [tag, '专题补充'],
    explanation,
  }
}

function blank(tag, stem, answer, explanation) {
  const answers = Array.isArray(answer) ? answer.map(String) : [String(answer)]
  return {
    type: 'blank',
    stem,
    options: [],
    correctAnswers: [answers[0]],
    acceptedAnswers: answers.slice(1),
    tags: [tag, '专题补充'],
    explanation,
  }
}

