function single(stem, options, answer, explanation, tags = []) {
  return {
    subjectId: 'data-structure',
    type: 'single',
    stem,
    options: options.map((text, index) => ({ key: String.fromCharCode(65 + index), text })),
    correctAnswers: [answer],
    tags: ['查找与排序', '第七套作业', ...tags],
    explanation,
    sourceTag: '图片例题',
  }
}

