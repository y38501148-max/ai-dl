function opt(values) {
  return values.map((text, index) => ({ key: String.fromCharCode(65 + index), text }))
}

function single(year, number, tag, stem, options, answer, explanation, extra = {}) {
  return {
    type: 'single',
    stem,
    options: opt(options),
    correctAnswers: [answer],
    tags: [tag, `${year}真题`, '非编程题'],
    explanation,
    ...extra,
  }
}

function blank(year, number, tag, stem, answer, explanation, extra = {}) {
  const answers = Array.isArray(answer) ? answer.map(String) : [String(answer)]
  return {
    type: 'blank',
    stem,
    options: [],
    correctAnswers: [answers[0]],
    acceptedAnswers: answers.slice(1),
    tags: [tag, `${year}真题`, '非编程题'],
    explanation,
    ...extra,
  }
}

