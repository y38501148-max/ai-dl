const fallbackExplanation = '先识别题目对应的基本定义或结构性质'
const invalidExplanation = explainedDsQuestions.find(
  (question) =>
    !question.explanation?.startsWith('思路：') ||
    question.explanation.includes(fallbackExplanation) ||
    /答案：|判分|给分/.test(question.explanation),
)
if (invalidExplanation) {
  throw new Error(`数据结构题解异常：${invalidExplanation.id}`)
}

function duplicateGroups(normalize) {
  const groups = new Map()
  for (const question of explainedDsQuestions) {
    const key = `${question.type}\t${normalize(question.stem)}`
    if (!groups.has(key)) groups.set(key, [])
    groups.get(key).push(question.id)
  }
  return [...groups.entries()].filter(([, ids]) => ids.length > 1)
}

const exactDuplicates = duplicateGroups((stem) => stem.replace(/\s+/g, ' ').trim())
if (exactDuplicates.length) {
  throw new Error(`数据结构题干存在精确重复：${exactDuplicates[0][1].join(',')}`)
}
const normalizedDuplicates = duplicateGroups((stem) => stem.replace(/\d+/g, '#').replace(/\s+/g, ' ').trim())
if (normalizedDuplicates.length) {
  throw new Error(`数据结构题干存在模板重复：${normalizedDuplicates[0][1].join(',')}`)
}

