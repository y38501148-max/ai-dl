function answerText(question) {
  if (question.type === 'blank') {
    return [question.correctAnswers?.[0], ...(question.acceptedAnswers ?? [])].filter(Boolean).join('；')
  }
  return (question.correctAnswers ?? []).map((key) => optionText(question, key)).join('；')
}

