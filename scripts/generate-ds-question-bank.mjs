const dsQuestions = []
let sourceNumber = 1

function addChoice(topic, stem, options, correctKey, extra = {}) {
  dsQuestions.push({
    id: `ds-${String(sourceNumber).padStart(3, '0')}`,
    sourceNumber,
    subjectId: 'data-structure',
    type: 'single',
    stem,
    options: options.map((text, index) => ({ key: String.fromCharCode(65 + index), text })),
    correctAnswers: [correctKey],
    tags: [topic],
    ...extra,
  })
  sourceNumber += 1
}

function addBlank(topic, stem, answers, extra = {}) {
  const [correct, ...aliases] = Array.isArray(answers) ? answers : [answers]
  dsQuestions.push({
    id: `ds-${String(sourceNumber).padStart(3, '0')}`,
    sourceNumber,
    subjectId: 'data-structure',
    type: 'blank',
    stem,
    options: [],
    correctAnswers: [String(correct)],
    acceptedAnswers: aliases.map(String),
    tags: [topic],
    ...extra,
  })
  sourceNumber += 1
}

function addGuidedChoice(topic, stem, options, correctKey, guide, extra = {}) {
  addChoice(topic, stem, options, correctKey, { ...extra, explanation: `思路：${guide}` })
}

function addGuidedBlank(topic, stem, answers, guide, extra = {}) {
  addBlank(topic, stem, answers, { ...extra, explanation: `思路：${guide}` })
}

